class Textures {
    static tx = [];
    static sheet;
    static loader = PIXI.Loader.shared;
    static img;
    static buffer;
    constructor(data, callback) {

        Textures.loader.add('titolo', 'assets/img/titolo.png').
            add('sfondo', 'assets/img/sfondo_titolo.png');

        Textures.buffer = new Float32Array(256 * 512 * 4);

        Textures.img = PIXI.BaseTexture.fromBuffer(Textures.buffer, 256, 512);
        Textures.SetColors([
            { r: 0, g: 0, b: 0, a: 0 },
            { r: 0.47, g: 0.47, b: 0.47, a: 1 },
            { r: 0.54, g: 0.32, b: 0.16, a: 1 },
            { r: 1, g: 1, b: 1, a: 1 },
        ]);

        Textures.sheet = new PIXI.Spritesheet(Textures.img, data);


        Textures.loader.load(
            () => {
                Textures.sheet.parse(callback);
            }

        );

    }

    static GetColor(x, y) {
        // 0 - 3

    }

    static PutPixel(x, y, col) {
        x = x * 4;
        y = y * 2;
        for (let dy = 0; dy < 2; dy++) {
            for (let dx = 0; dx < 4; dx++) {
                Textures.buffer[(x + dx) * 4 + (y + dy) * 256 * 4 + 0] = col.r;
                Textures.buffer[(x + dx) * 4 + (y + dy) * 256 * 4 + 1] = col.g;
                Textures.buffer[(x + dx) * 4 + (y + dy) * 256 * 4 + 2] = col.b;
                Textures.buffer[(x + dx) * 4 + (y + dy) * 256 * 4 + 3] = col.a;
            }
        }

    }

    static SetColors(cols) {
        
        let fix = [
            { r: 0, g: 0, b: 0, a: 0 },
            { r: 0.47, g: 0.47, b: 0.47, a: 1 },
            { r: 0.54, g: 0.32, b: 0.16, a: 1 },
            { r: 1, g: 1, b: 1, a: 1 },
        ];
        
        for (let y = 0; y < 256; y++) {
            let chars = dati[y].split("");
            for (let x = 0; x < 32; x++) {

                let b = parseInt(chars[x], 16);
                let b1 = b >> 2;
                let b2 = b & 3;
                if (y < 208) {
                    Textures.PutPixel(x * 2, y, cols[b1]);
                    Textures.PutPixel(x * 2 + 1, y, cols[b2]);
                } else {
                    Textures.PutPixel(x * 2, y, fix[b1]);
                    Textures.PutPixel(x * 2 + 1, y, fix[b2]);
               
                }
                // da un certo punto in poi, riempire solo di bianco!!!
            }
        }


        Textures.img.resource.update();
    }
}


